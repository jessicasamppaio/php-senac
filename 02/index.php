<?php
# Strings


# Para unir duas ou mais strings em PHP, usamos o operador de concatenação.
$nome = "Jéssica";
$sobrenome = "Sampaio";

echo $nome." ".$sobrenome; // Saída: Jéssica Sampaio
echo "<br>";

# Para obter o comprimento de uma string em PHP, usamos a função strlen()
$texto = "Olá, mundo";
echo strlen($texto);


?>